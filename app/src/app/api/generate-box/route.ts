import { NextRequest, NextResponse } from 'next/server';
import { GoogleGenerativeAI } from '@google/generative-ai';
import sharp from 'sharp';
import { buildSpriteSheetPrompt } from '@/lib/boxStyles';
import { DEFAULT_BOX_DIMENSIONS } from '@/lib/config';
import type { BoxDimensions, DrawerStyle } from '@/lib/types';

// Green chroma key fallback + raw sprite for client-side ML bg removal
export const maxDuration = 60;

interface GenerateBoxRequest {
  style: DrawerStyle;
  dimensions?: BoxDimensions;
}

export async function POST(request: NextRequest) {
  let builtPrompt: string | undefined;

  try {
    const body: GenerateBoxRequest = await request.json();
    const { style, dimensions } = body;

    builtPrompt = buildSpriteSheetPrompt(style, dimensions ?? DEFAULT_BOX_DIMENSIONS);

    const apiKey = process.env.GOOGLE_AI_STUDIO_KEY;
    if (!apiKey) {
      return NextResponse.json(
        { error: 'GOOGLE_AI_STUDIO_KEY not configured', prompt: builtPrompt },
        { status: 500 }
      );
    }

    const genAI = new GoogleGenerativeAI(apiKey);
    const model = genAI.getGenerativeModel({
      model: 'gemini-3.1-flash-image-preview',
      // @ts-expect-error - responseModalities not yet in types but supported by API
      generationConfig: { responseModalities: ['Text', 'Image'] },
    });

    // Single prompt for all 5 states as a sprite sheet
    const result = await model.generateContent([{ text: builtPrompt }]);
    const response = result.response;

    // Extract image from response
    let imageBase64: string | null = null;
    const candidates = response.candidates;
    if (candidates && candidates.length > 0) {
      const contentParts = candidates[0].content?.parts ?? [];
      for (const part of contentParts) {
        if ((part as any).inlineData) {
          imageBase64 = (part as any).inlineData.data;
          break;
        }
      }
    }

    if (!imageBase64) {
      return NextResponse.json(
        { error: 'No image generated by Gemini', prompt: builtPrompt },
        { status: 500 }
      );
    }

    // Remove green background from the sprite sheet
    let spriteBuffer: Buffer = Buffer.from(imageBase64, 'base64');
    const metadata = await sharp(spriteBuffer).metadata();
    let fullWidth = metadata.width || 2500;
    let fullHeight = metadata.height || 500;
    let ratioWarning: string | undefined;

    // Validate sprite ratio — must be ~5:1 for 5 square-ish frames.
    // Drawer shape ratio only affects the drawn object, not the frame dimensions.
    const ratio = fullWidth / fullHeight;
    if (ratio < 4) {
      // Gently resize toward 5:1, but cap at 1.5× stretch to avoid distortion
      // when Gemini fails to produce a proper sprite sheet.
      const idealWidth = fullHeight * 5;
      const maxWidth = Math.round(fullWidth * 1.5);
      const targetWidth = Math.min(idealWidth, maxWidth);
      ratioWarning = `Gemini returned ${fullWidth}×${fullHeight} (${ratio.toFixed(1)}:1), resized to ${targetWidth}×${fullHeight}`;
      spriteBuffer = await sharp(spriteBuffer)
        .resize(targetWidth, fullHeight, { fit: 'contain', background: { r: 0, g: 255, b: 0, alpha: 1 } })
        .toBuffer();
      fullWidth = targetWidth;
    }

    let bgRemoval: 'vision' | 'chroma-fallback';
    let visionObjects = 0;
    let processedBase64: string;

    const visionApiKey = process.env.GOOGLE_CLOUD_VISION_API_KEY;

    if (visionApiKey) {
      // Vision API-assisted green chroma key removal
      try {
        const visionRes = await fetch(
          `https://vision.googleapis.com/v1/images:annotate?key=${visionApiKey}`,
          {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
              requests: [{
                image: { content: imageBase64 },
                features: [{ type: 'OBJECT_LOCALIZATION', maxResults: 10 }],
              }],
            }),
          }
        );
        const visionData = await visionRes.json();
        const objects = visionData.responses?.[0]?.localizedObjectAnnotations ?? [];
        visionObjects = objects.length;

        // Build object mask regions from Vision API bounding polys
        const objectRegions = objects.map((obj: any) => {
          const vertices = obj.boundingPoly?.normalizedVertices ?? [];
          return vertices.map((v: any) => ({
            x: Math.round((v.x || 0) * fullWidth),
            y: Math.round((v.y || 0) * fullHeight),
          }));
        });

        processedBase64 = await removeGreenWithVision(spriteBuffer, fullWidth, fullHeight, objectRegions);
        bgRemoval = 'vision';
      } catch (visionErr) {
        console.warn('Vision API failed, falling back to pure chroma key:', visionErr);
        processedBase64 = await removeGreenChromaKey(spriteBuffer);
        bgRemoval = 'chroma-fallback';
      }
    } else {
      // Pure green chroma key removal (no Vision API key)
      processedBase64 = await removeGreenChromaKey(spriteBuffer);
      bgRemoval = 'chroma-fallback';
    }

    // Compute active area — tight bounding box of non-transparent pixels
    const activeArea = await computeActiveArea(processedBase64, fullWidth, fullHeight, 5);

    return NextResponse.json({
      sprite: processedBase64,
      prompt: builtPrompt,
      mimeType: 'image/png',
      spriteSize: { width: fullWidth, height: fullHeight, frameCount: 5 },
      activeArea,
      bgRemoval,
      visionObjects,
      ...(ratioWarning && { ratioWarning }),
    });
  } catch (error: any) {
    console.error('Box generation error:', error);
    return NextResponse.json(
      { error: error.message || 'Generation failed', details: String(error), prompt: builtPrompt },
      { status: 500 }
    );
  }
}

// ─── YCbCr Chroma Key Helpers ──────────────────────────────────────────
// Industry-standard broadcast chroma key using Euclidean distance in CbCr
// chroma plane (gc-films.com colorclose algorithm). Separates luminance
// from chrominance so green detection works across all brightness levels.

/** RGB → Cb (JPEG-standard coefficients) */
function rgb2cb(r: number, g: number, b: number): number {
  return 128 + (-0.168736 * r - 0.331264 * g + 0.5 * b);
}
/** RGB → Cr (JPEG-standard coefficients) */
function rgb2cr(r: number, g: number, b: number): number {
  return 128 + (0.5 * r - 0.418688 * g - 0.081312 * b);
}

// Pre-computed CbCr for key color #00FF00
const KEY_CB = rgb2cb(0, 255, 0);
const KEY_CR = rgb2cr(0, 255, 0);

/**
 * Broadcast-standard chroma key distance function.
 * Returns 0.0 (fully green → transparent) to 1.0 (not green → opaque).
 * tola = inner tolerance (fully keyed), tolb = outer tolerance (fully kept).
 */
function colorclose(r: number, g: number, b: number, tola: number, tolb: number): number {
  const cb = rgb2cb(r, g, b);
  const cr = rgb2cr(r, g, b);
  const dist = Math.sqrt((KEY_CB - cb) ** 2 + (KEY_CR - cr) ** 2);
  if (dist < tola) return 0.0;
  if (dist < tolb) return (dist - tola) / (tolb - tola);
  return 1.0;
}

/**
 * Professional spill suppression: channel-limiting method.
 * Reduces green toward avg(R, B) — stronger on semi-transparent edge pixels,
 * gentler on fully opaque interior pixels.
 */
function despill(pixels: Uint8Array): void {
  for (let i = 0; i < pixels.length; i += 4) {
    const r = pixels[i], g = pixels[i + 1], b = pixels[i + 2], a = pixels[i + 3];
    if (a === 0) continue;
    const avgRB = (r + b) / 2;
    if (g <= avgRB) continue;
    if (a < 240) {
      // Semi-transparent (edge): strong suppression — clamp G to avg(R,B)
      pixels[i + 1] = Math.round(Math.min(g, avgRB));
    } else {
      // Fully opaque: gentle suppression — reduce excess by 30%
      pixels[i + 1] = Math.round(g - (g - avgRB) * 0.3);
    }
  }
}

/**
 * Post-process alpha channel: morphological open (erode→dilate) to remove
 * 1px green speck artifacts, median to smooth jagged edges, blur to feather.
 */
async function cleanAlpha(
  pixels: Uint8Array, w: number, h: number,
): Promise<void> {
  // Extract alpha into a single-channel buffer
  const alpha = Buffer.alloc(w * h);
  for (let i = 0; i < w * h; i++) alpha[i] = pixels[i * 4 + 3];

  // Morphological open (erode→dilate): removes isolated opaque specks
  // Then median(3) smooths jagged edges, blur(0.8) feathers transitions
  const cleaned = await sharp(alpha, { raw: { width: w, height: h, channels: 1 } })
    .erode()
    .dilate()
    .median(3)
    .blur(0.8)
    .raw()
    .toBuffer();

  // Write cleaned alpha back into the RGBA pixel array
  const out = new Uint8Array(cleaned.buffer, cleaned.byteOffset, cleaned.length);
  for (let i = 0; i < w * h; i++) pixels[i * 4 + 3] = out[i];
}

/**
 * Remove green background using Vision API object bounds for edge refinement.
 * Uses YCbCr colorclose with distance-adaptive tolerances:
 * - Far from objects: aggressive keying (low tolerances)
 * - Near object edges: conservative keying (high tolerances)
 * - Inside objects: very conservative (only remove strong greens)
 */
async function removeGreenWithVision(
  buffer: Buffer,
  width: number,
  height: number,
  objectRegions: { x: number; y: number }[][],
): Promise<string> {
  const { data, info } = await sharp(buffer)
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  const pixels = new Uint8Array(data.buffer, data.byteOffset, data.length);
  const edgeDistance = buildEdgeDistanceMap(width, height, objectRegions);

  // YCbCr chroma key with distance-adaptive tolerances
  for (let i = 0; i < pixels.length; i += 4) {
    const r = pixels[i], g = pixels[i + 1], b = pixels[i + 2];
    const dist = edgeDistance[i / 4];

    let tola: number, tolb: number;
    if (dist > 10) {
      tola = 15; tolb = 40;   // Far from object: aggressive
    } else if (dist > 0) {
      tola = 25; tolb = 65;   // Near object edge: conservative
    } else {
      tola = 30; tolb = 75;   // Inside object: very conservative
    }

    pixels[i + 3] = Math.round(colorclose(r, g, b, tola, tolb) * 255);
  }

  await cleanAlpha(pixels, info.width, info.height);
  despill(pixels);

  const result = await sharp(Buffer.from(pixels.buffer, pixels.byteOffset, pixels.length), {
    raw: { width: info.width, height: info.height, channels: 4 },
  }).png().toBuffer();

  return result.toString('base64');
}

/**
 * Pure green background removal using YCbCr colorclose algorithm.
 * No Vision API needed. Uses fixed tolerances with morphological cleanup.
 */
async function removeGreenChromaKey(
  buffer: Buffer,
): Promise<string> {
  const { data, info } = await sharp(buffer)
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  const pixels = new Uint8Array(data.buffer, data.byteOffset, data.length);

  // YCbCr chroma key with fixed tolerances
  for (let i = 0; i < pixels.length; i += 4) {
    pixels[i + 3] = Math.round(colorclose(pixels[i], pixels[i + 1], pixels[i + 2], 20, 55) * 255);
  }

  await cleanAlpha(pixels, info.width, info.height);
  despill(pixels);

  const result = await sharp(Buffer.from(pixels.buffer, pixels.byteOffset, pixels.length), {
    raw: { width: info.width, height: info.height, channels: 4 },
  }).png().toBuffer();

  return result.toString('base64');
}

/**
 * Build a distance map from Vision API object bounding box edges.
 * Positive = outside object, 0 = on edge, negative = inside object.
 */
function buildEdgeDistanceMap(
  width: number,
  height: number,
  objectRegions: { x: number; y: number }[][],
): Float32Array {
  const MARGIN = 15;
  const map = new Float32Array(width * height).fill(999);

  for (const region of objectRegions) {
    if (region.length < 2) continue;
    const xs = region.map(p => p.x);
    const ys = region.map(p => p.y);
    const minX = Math.max(0, Math.min(...xs));
    const maxX = Math.min(width - 1, Math.max(...xs));
    const minY = Math.max(0, Math.min(...ys));
    const maxY = Math.min(height - 1, Math.max(...ys));

    for (let y = Math.max(0, minY - MARGIN); y <= Math.min(height - 1, maxY + MARGIN); y++) {
      for (let x = Math.max(0, minX - MARGIN); x <= Math.min(width - 1, maxX + MARGIN); x++) {
        const distToEdge = Math.min(
          Math.abs(x - minX), Math.abs(x - maxX),
          Math.abs(y - minY), Math.abs(y - maxY),
        );
        const inside = x >= minX && x <= maxX && y >= minY && y <= maxY;
        const dist = inside ? -distToEdge : distToEdge;
        map[y * width + x] = Math.min(map[y * width + x], dist);
      }
    }
  }

  return map;
}

/**
 * Scan the bg-removed sprite for non-transparent pixels across all frames.
 * Returns a normalized (0-1) bounding box representing the union of active
 * content across all 5 frames — relative to each frame's own dimensions.
 */
async function computeActiveArea(
  base64: string,
  spriteWidth: number,
  spriteHeight: number,
  frameCount: number,
): Promise<{ x: number; y: number; width: number; height: number }> {
  const { data } = await sharp(Buffer.from(base64, 'base64'))
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  const pixels = new Uint8Array(data.buffer, data.byteOffset, data.length);
  const frameW = Math.floor(spriteWidth / frameCount);

  // Find the tightest bounding box of non-transparent pixels across all frames
  // Coordinates are relative to each frame (not the full sprite)
  let minX = frameW;
  let minY = spriteHeight;
  let maxX = 0;
  let maxY = 0;

  for (let frame = 0; frame < frameCount; frame++) {
    const offsetX = frame * frameW;
    for (let y = 0; y < spriteHeight; y++) {
      for (let x = 0; x < frameW; x++) {
        const pixelIdx = (y * spriteWidth + (offsetX + x)) * 4;
        if (pixels[pixelIdx + 3] > 10) {
          minX = Math.min(minX, x);
          maxX = Math.max(maxX, x);
          minY = Math.min(minY, y);
          maxY = Math.max(maxY, y);
        }
      }
    }
  }

  // Add a small padding (2% of frame dimensions)
  const padX = Math.round(frameW * 0.02);
  const padY = Math.round(spriteHeight * 0.02);
  minX = Math.max(0, minX - padX);
  minY = Math.max(0, minY - padY);
  maxX = Math.min(frameW - 1, maxX + padX);
  maxY = Math.min(spriteHeight - 1, maxY + padY);

  return {
    x: minX / frameW,
    y: minY / spriteHeight,
    width: (maxX - minX + 1) / frameW,
    height: (maxY - minY + 1) / spriteHeight,
  };
}
